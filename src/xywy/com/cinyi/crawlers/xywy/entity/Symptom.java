public class Symptom {
	
	private int id;

	// 名称
	private String name;

	// 拼音首字母
	private String spell;

	// 症状概述
	private String remark;

	// 症状原因
	private String reason;

	// 症状检查
	private String checkupRemark;

	// 鉴别诊断
	private String jbzd;

	// 缓解方法
	private String hjff;

	// 图片
	private byte[] photo;

	// 患病部位
	private String bodyRef;

	// 所属科室
	private String specialtyRef;

	// 相关疾病
	private String illnessRef;

	// 相关检查
	private String checkupRef;

	// 相关症状
	private String symptomRef;

	public String getBodyRef() {
		return bodyRef;
	}

	public void setBodyRef(String bodyRef) {
		this.bodyRef = bodyRef;
	}

	public String getSpecialtyRef() {
		return specialtyRef;
	}

	public void setSpecialtyRef(String specialtyRef) {
		this.specialtyRef = specialtyRef;
	}

	public String getIllnessRef() {
		return illnessRef;
	}

	public void setIllnessRef(String illnessRef) {
		this.illnessRef = illnessRef;
	}

	public String getCheckupRef() {
		return checkupRef;
	}

	public void setCheckupRef(String checkupRef) {
		this.checkupRef = checkupRef;
	}

	public String getSymptomRef() {
		return symptomRef;
	}

	public void setSymptomRef(String symptomRef) {
		this.symptomRef = symptomRef;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSpell() {
		return spell;
	}

	public void setSpell(String spell) {
		this.spell = spell;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getCheckupRemark() {
		return checkupRemark;
	}

	public void setCheckupRemark(String jianCha) {
		this.checkupRemark = jianCha;
	}

	public String getJbzd() {
		return jbzd;
	}

	public void setJbzd(String jbzd) {
		this.jbzd = jbzd;
	}

	public String getHjff() {
		return hjff;
	}

	public void setHjff(String hjff) {
		this.hjff = hjff;
	}

	public byte[] getPhoto() {
		return photo;
	}

	public void setPhoto(byte[] photo) {
		this.photo = photo;
	}
}
