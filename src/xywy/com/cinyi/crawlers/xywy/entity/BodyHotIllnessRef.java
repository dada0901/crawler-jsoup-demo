package com.cinyi.crawlers.xywy.entity;

public class BodyHotIllnessRef {

	private int id;
	private int bodyId;
	private int hotIllnessId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getBodyId() {
		return bodyId;
	}

	public void setBodyId(int bodyId) {
		this.bodyId = bodyId;
	}

	public int getHotIllnessId() {
		return hotIllnessId;
	}

	public void setHotIllnessId(int hotIllnessId) {
		this.hotIllnessId = hotIllnessId;
	}

	public BodyHotIllnessRef(int bodyId, int hotIllnessId) {
		super();
		this.bodyId = bodyId;
		this.hotIllnessId = hotIllnessId;
	}

}
